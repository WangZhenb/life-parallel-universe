// 所有风格映射
const DATA = { gentle, dark, wasteland, scifi, random }

/**
 * 生成结构化故事（新格式）
 * @param {string} userChoice - 用户选择文本
 * @param {string} style - 结局风格
 * @returns {{ title, summary, timeline, ending, choices }}
 */
function generateStructured(userChoice, style = 'gentle') {
  const stories = DATA[style] || DATA.random
  const story = pick(stories)

  return {
    title: story.title,
    summary: story.summary,
    timeline: story.timeline.map(t => ({ year: t.year, event: t.event })),
    ending: story.ending,
    choices: [...story.choices]
  }
}

// ── 向后兼容：旧格式（纯文本组装） ──
function generateStoryLegacy(userChoice, style = 'gentle') {
  const s = generateStructured(userChoice, style)
  let text = `"${userChoice.trim() || '那个决定'}"\n\n`
  text += s.summary + '\n\n'
  s.timeline.forEach(t => { text += `${t.year}：${t.event}\n\n` })
  text += s.ending
  return text
}

function generateTitleLegacy(userChoice, style) {
  const styleMap = {
    gentle: '温柔宇宙', dark: '黑暗宇宙',
    wasteland: '废土宇宙', scifi: '科幻宇宙', random: '未知宇宙'
  }
  const universeName = styleMap[style] || '平行宇宙'
  const choice = userChoice.trim().slice(0, 12)
  return `${choice || '那个选择'} · ${universeName}`
}

module.exports = {
  generateStructured,
  generateStory: generateStoryLegacy,
  generateTitle: generateTitleLegacy
}
